#!/usr/bin/env python3
# -*- coding: UTF-8 -*-

def print_red(text):
    print('\033[91m' + str(text) + '\033[0m')

def print_green(text):
    print('\033[92m' + str(text) + '\033[0m')

def print_yellow(text):
    print('\033[93m' + str(text) + '\033[0m')

def print_blue(text):
    print('\033[94m' + str(text) + '\033[0m')

def print_magenta(text):
    print('\033[95m' + str(text) + '\033[0m')

def print_cyan(text):
    print('\033[96m' + str(text) + '\033[0m')

